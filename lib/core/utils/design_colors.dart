import 'dart:ui';

/// A utility abstract class that provides predefined constant color values from UI design.
abstract class DesignColors {
  /// A light background color (hex: #FBFBF9).
  static const Color backgroundColor = Color(0xffFBFBF9);

  /// A deep dark blue color (hex: #001434).
  static const Color deepDarkBlue = Color(0xff001434);
  static const Color white = Color(0xffffffff);
  static const Color red = Color(0xffC70600);
  static const Color cyan = Color(0xff3D9CFB);
  static const Color royalBlue = Color(0xff0077CC);
  static const Color grey = Color(0xffB8B8B8);
}
